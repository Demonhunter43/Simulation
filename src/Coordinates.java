public class Coordinates {
    private int x,y;

    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }
    public int getX() {
        return x;
    }
    public void setCoordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }
}